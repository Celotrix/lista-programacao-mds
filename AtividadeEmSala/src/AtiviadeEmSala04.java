public class AtiviadeEmSala04 {
    public static void main(String[] args){
        int x = 0;

        x += 1;
        System.out.println("x += 1  RESULTADO :" + x);
        x++;
        System.out.println("x++     RESULTADO :" + x);
        x-= 2;
        System.out.println("x-= 2   RESULTADO :" + x);
        x--;
        System.out.println("x--     RESULTADO :" + x);




    }
}
